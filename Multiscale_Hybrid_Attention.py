class TP_Block(nn.Module):
    def __init__(self, *, image_size=512, patch_size=16, inc, outc):
        super().__init__()
        num_patches = (image_size // patch_size) ** 2 #16*16
        dim = inc*patch_size**2
        self.img_size = image_size

        self.se = SE_block(inc)

        self.conv1 = DeformConv2d(inc=inc, outc=dim, kernel_size=patch_size, stride=patch_size, bias=False)

        self.pos_emb = nn.Parameter(torch.randn(1, dim, num_patches))  # 位置编码，获取一组正态分布的数据用于训练
        self.transformer = nn.TransformerEncoderLayer(d_model=num_patches, nhead=1)

        self.conv2 = nn.Conv2d(in_channels=inc, out_channels=outc, kernel_size=3, padding=1)


    def forward(self, x:torch.Tensor):

        x = self.se(x)
        #print('x', x.shape)

        x1 = self.conv1(x)  #(b, dim, 16, 16)
        #print('x1', x1.shape)
        x1 = torch.reshape(x1, (x1.shape[0], x1.shape[1], -1))
        #print('x1', x1.shape)
        x1 = torch.add(x1, self.pos_emb.to(x1.dtype))
        #print('x1', x1.shape)

        x1 = self.transformer(x1)

        x1 = torch.reshape(x1, (x.shape[0], x.shape[1], x.shape[2], x.shape[3]))

        out = self.conv2(x1)

        return out





